/** Returns true if the player has a Chinese name translation. */
export function hasChineseName(player) {
  if (!player) return false
  return (`${player.first_name} ${player.last_name}`) in ZH_NAMES
}

/**
 * Full display name.
 * English: "FirstName LastName"
 * Chinese: translated full name, or English fallback if not in table.
 */
export function getPlayerName(player, lang) {
  if (!player) return ''
  if (lang !== 'zh') return `${player.first_name} ${player.last_name}`
  const key = `${player.first_name} ${player.last_name}`
  return ZH_NAMES[key] ?? `${player.first_name} ${player.last_name}`
}

/**
 * Short display name — for card bottoms, bench chips, small labels.
 * English: player.last_name
 * Chinese: text after last · in the full name, or override, or English fallback.
 */
export function getPlayerShortName(player, lang) {
  if (!player) return ''
  if (lang !== 'zh') return player.last_name
  const key = `${player.first_name} ${player.last_name}`
  if (ZH_SHORT_OVERRIDES[key]) return ZH_SHORT_OVERRIDES[key]
  const full = ZH_NAMES[key]
  if (!full) return player.last_name
  const dotIdx = full.lastIndexOf('·')
  return dotIdx >= 0 ? full.slice(dotIdx + 1) : full
}
